    private void jButton_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_pesquisarActionPerformed
        // TODO add your handling code here:
        String numprocesso=jTextField_numProc.getText();
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from Processos where NumProcesso='"+numprocesso+"'");
            if(rs.next()){
              jTextField_nomReq.setText(rs.getString(1));  
              jTextField_processo.setText(rs.getString(2));
              jTextField_numProcesso.setText(rs.getString(3));
              jTextField_requer.setText(rs.getString(4));
              jTextField_form.setText(rs.getString(5));
              jTextField_certifi.setText(rs.getString(6));
              jTextField_bi.setText(rs.getString(7));
              jTextFieldgenero.setText(rs.getString(8));
              jTextField_numProc.setEditable(false);
            }else{
                JOptionPane.showMessageDialog(null,"Numero de processo nao existente");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton_pesquisarActionPerformed

    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
        // TODO add your handling code here:
        String numprocesso1=jTextField_numProc.getText();
        String Nome=jTextField_nomReq.getText();
        String Processo=jTextField_processo.getText();
        String NumProcesso=jTextField_numProcesso.getText();
        String Requerimento=jTextField_requer.getText();
        String Formulario=jTextField_form.getText();
        String Certificado=jTextField_certifi.getText();
        String Fotocopia=jTextField_bi.getText();
        String Genero=jTextFieldgenero.getText();
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            st.executeUpdate("update Processos Nome='"+Nome+"',Processo='"+Processo+"',NumProcesso='"+NumProcesso+"',Requerimento='"+Requerimento+"',Formulario='"+Formulario+"',Certificado='"+Certificado+"',Fotocopia='"+Fotocopia+"',Genero='"+Genero+"'");
            JOptionPane.showMessageDialog(null,"Actualizado com sucesso");
            setVisible(false);
            new Actualizar().setVisible(true);
            dispose();
            
        }catch(Exception e){}
        
    }//GEN-LAST:event_jButton_actualizarActionPerformed

